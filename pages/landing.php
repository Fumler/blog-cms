    <h1>What is a blog?</h1>
    <p>A blog can be your personal diary, or it can be a memo to the world. You can discuss many things in many forms and reach out to many different people.</p>
    <p>The blog is what you want it to be. There are millions of blogs in different colors, shapes and sizes and there are no rules.</p>
    <p>In short, a blog is a website where you can write whatever you want, and where the newest material is shown first. So that the viewers always have the latest news. And the users can comment on your posts to create a debate about different topics.</p>

